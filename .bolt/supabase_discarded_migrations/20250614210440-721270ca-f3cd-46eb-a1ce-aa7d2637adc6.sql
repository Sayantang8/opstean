
-- Add RLS policies for contacts table to allow admin operations
CREATE POLICY "Admin users can delete contacts" 
  ON public.contacts 
  FOR DELETE 
  USING (
    EXISTS (
      SELECT 1 FROM public.profiles 
      WHERE profiles.id = auth.uid() 
      AND profiles.role = 'admin'
    )
  );

